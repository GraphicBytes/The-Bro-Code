      <h2>Est 2012</h2>

      <p>References to a Bro Code are decades old and it isn’t clear when and where the concept began, so we set out to consolidate all views and ideas on what a Bro Code should be. These are the rules of manliness and gentlemanly behaviour, a go to reference to hold yourself and your fellow Bros to a higher standard.</p>

      <p>We started this project in 2012 as a blog collecting suggestions via Facebook comments, but this wasn’t right. The Bro Code available here was mistakenly editorialised by ourselves picking the suggestions we liked best, and not necessarily the Bro Codes that rightfully deserved to be included. So, we decided to throw together this website which aims to provide the legislative tools needed for anyone and everyone to sign up and have their say on what The Bro Code is.</p>

      <h2>PROJECT AMBITIONS</h2>

      <p>This was a pet project by what are now experienced web developers. The team behind this project build SoA based platforms for some of the biggest multi-nationals in the world, and we plan on redeveloping this project from the ground up.</p>

      <p>The redevelopment will use a modern tech stack, and be available as a website and app. First and foremost, we wanted The Bro Code to be fully mouldable by the members, and we have that available now, so the first stage will be to translate the current functions into our new tech stack.</p>

      <p>From that point forward, this will grow and evolve into whatever the community want this to evolve into, by introducing mechanics that will let our community suggest and vote on what new features and tools to develop next.</p>

